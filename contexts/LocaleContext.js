const LocaleContext = createContext(undefined);

const detectLocale = () => {
  if (typeof window === 'undefined') return 'en';
  const browserLanguage = String(window.navigator.language || '').toLowerCase();
  return browserLanguage.startsWith('fr') ? 'fr' : 'en';
};

const interpolate = (template, vars) =>
  String(template).replace(/\{\{(\w+)\}\}/g, (_, key) => String(vars?.[key] ?? ''));

export function LocaleProvider({ children }) {
  const [locale, setLocale] = useState('en');

  useEffect(() => {
    if (typeof window === 'undefined') return;
    const stored = window.localStorage.getItem(STORAGE_KEY);
    const nextLocale = SUPPORTED_LOCALES.includes(stored) ? stored : detectLocale();
    setLocale(nextLocale);
  }, []);

  useEffect(() => {
    if (typeof window === 'undefined') return;
    window.localStorage.setItem(STORAGE_KEY, locale);
  }, [locale]);

  const value = useMemo(() => {
    const t = (key, vars) => {
      const table = messages[locale] || messages.en;
      const fallbackTable = messages.en;
      const raw = table[key] ?? fallbackTable[key] ?? key;
      return interpolate(raw, vars);
    };
    return { locale, setLocale, t, supportedLocales: SUPPORTED_LOCALES };
  }, [locale]);

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const ctx = useContext(LocaleContext);
  if (!ctx) throw new Error('useLocale must be used inside LocaleProvider');
  return ctx;
}
